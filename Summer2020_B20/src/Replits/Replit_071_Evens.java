package Replits;

public class Replit_071_Evens {
    public static void main(String[] args) {
        /*
        Write a program that will print out even numbers from 2 to 100 through the comma.
         */

        for (int a = 2; a <100; a ++) {
            if (a % 2 == 0) {
                System.out.print(a + ",");
            }
        }




    }
}
