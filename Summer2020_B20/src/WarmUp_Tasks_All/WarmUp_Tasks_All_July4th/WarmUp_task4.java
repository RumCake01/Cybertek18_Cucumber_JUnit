package WarmUp_Tasks_All.WarmUp_Tasks_All_July4th;

public class WarmUp_task4 {
    public static void main(String[] args) {
        /*4. write a program that can calculate the sum of all numbers between 1 to 100*/

        int a;
        int sum = 0;
        for (a = 1; a<=100; a ++){
            sum +=a;
        }
        System.out.print("\nThis is the summary of numbers between 1 to 100: "  + sum + "\n");
    }
}
