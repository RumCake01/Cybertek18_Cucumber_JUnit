package Day19_For_Loop;

public class For_Loops_Without_CurlyBraces {

    public static void main(String[] args) {

        // DO NOT USE IT, BUT GOOD TO KNOW


        for(int a= 5; a<=10; a ++){
            System.out.println("hello");
            System.out.println("How are you");

        } // this statement will each of the statements 6 times
        System.out.println("===========================");
        // below is the example without curly braces but NEVER REPEAT IT

        for(int i = 1; i<=5; i++)
            System.out.println("hello");
            System.out.println("How are you");
        System.out.println("===========================");







    }
}
