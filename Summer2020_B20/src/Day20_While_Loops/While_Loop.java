package Day20_While_Loops;

public class While_Loop {
    public static void main(String[] args) {

        if(true){
            System.out.println("hello world");
        }
        System.out.println("==================================");

        while(true){ // repeated If statement, it will only stop if the condition is false
            System.out.println("hello world");
        }





    }
}
